 <CENTER>
   <P ALIGN="LEFT">Much of this software is targetted at the Fidonet community, although other users might find something here to tickle their fancy. Fidonet and me go back a long way, and because of this I've turned my compiler on whenever I've encountered a problem I've been able to fix any other way. You know, a niggly little problem with anothers software or an inability to find something that does <I>exactly</I> what you want it to. Most of the programs below are short, sweet and without frills, but hopefully you might be able to make use of them. All of these programs are written in C with Borland's Turbo C/C++ V.3 for DOS, using the excellent IDE. No attempt at portability has been made, but with a bit of work many of the source files (where included) can be used.</P>
   <P ALIGN="LEFT">Where the C source is given, I retain no copyright and you are welcome to use it for your own programs. However - I would appreciate a mention in the docs or program somewhere...</P>
   <P ALIGN="LEFT">There is also a <A HREF="#UTILS" TARGET="">DOS Utility</A> section below which contains some little tools I wrote and still use very often.</P>
